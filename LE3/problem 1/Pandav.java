class Pandav extends Bharatvanshi {
    // Specific implementation for Pandavs
    public void fight() {
        System.out.println("Pandav is fighting bravely!");
    }
    
       // Pandavs are obedient
    public void obey() {
        System.out.println("Pandav is obedient.");
    }

    // Kindness is an adjective, not a verb
    public String kind() {
        return "Pandav is kind.";
    }
}