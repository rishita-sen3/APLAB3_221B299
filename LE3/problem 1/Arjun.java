class Arjun extends Pandav {
    // Arjun is very obedient
    @Override
    public void obey() {
        System.out.println("Arjun is very obedient.");
    }

    // Arjun is very kind
    @Override
    public String kind() {
        return "Arjun is very kind.";
    }
}